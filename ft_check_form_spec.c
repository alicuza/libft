#include "ft_printf.h"

int	ft_check_form_spec(const char **s)
{
	if (**s == '%')
	{
		(*s)++;
		if (!(**s))
			return (-1);
		if (**s == '%')
			return (0);
		return (1);
	}
	return (0);
}
